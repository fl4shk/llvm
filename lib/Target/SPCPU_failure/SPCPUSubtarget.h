#pragma once

#include "SPCPU.h"
#include "SPCPUFrameLowering.h"
#include "SPCPUISelLowering.h"
#include "SPCPUInstrInfo.h"
#include "SPCPUSelectionDAGInfo.h"
#include "SPCPUSubtarget.h"
#include "llvm/Target/TargetMachine.h"
#include "llvm/Target/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "SPCPUGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class SPCPUSubtarget : public SPCPUGenSubtargetInfo {
  virtual void anchor();

private:
  const DataLayout DL;       // Calculates type size & alignment.
  SPCPUInstrInfo InstrInfo;
  SPCPUTargetLowering TLInfo;
  SPCPUSelectionDAGInfo TSInfo;
  SPCPUFrameLowering FrameLowering;
  InstrItineraryData InstrItins;

public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  ///
  SPCPUSubtarget(const Triple &TT, StringRef CPU,
               StringRef FS, SPCPUTargetMachine &TM);

  /// getInstrItins - Return the instruction itineraries based on subtarget
  /// selection.
  const InstrItineraryData *getInstrItineraryData() const override {
    return &InstrItins;
  }
  const SPCPUInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const SPCPURegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }
  const SPCPUTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const SPCPUFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const SPCPUSelectionDAGInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);
};
} // End llvm namespace
