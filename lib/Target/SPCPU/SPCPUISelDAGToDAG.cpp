/// SPCPUDAGToDAGISel - SPCPU specific code to select SPCPU machine
/// instructions for SelectionDAG operations.
///
namespace {
class SPCPUDAGToDAGISel : public SelectionDAGISel {
  const SPCPUSubtarget &Subtarget;

public:
  explicit SPCPUDAGToDAGISel(SPCPUTargetMachine &TM, CodeGenOpt::Level OptLevel)
      : SelectionDAGISel(TM, OptLevel), Subtarget(*TM.getSubtargetImpl()) {}

  void Select(SDNode *N) override;

  bool SelectAddr(SDValue Addr, SDValue &Base, SDValue &Offset);

  virtual llvm::StringRef getPassName() const override {
    return "SPCPU DAG->DAG Pattern Instruction Selection";
  }

private:

// Include the pieces autogenerated from the target description.
#include "SPCPUGenDAGISel.inc"
};
} // end anonymous namespace

bool SPCPUDAGToDAGISel::SelectAddr(SDValue Addr, SDValue &Base, SDValue &Offset) {
  if (FrameIndexSDNode *FIN = dyn_cast<FrameIndexSDNode>(Addr)) {
    EVT PtrVT = getTargetLowering()->getPointerTy(CurDAG->getDataLayout());
    Base = CurDAG->getTargetFrameIndex(FIN->getIndex(), PtrVT);
    Offset = CurDAG->getTargetConstant(0, Addr, MVT::i32);
    return true;
  }
  if (Addr.getOpcode() == ISD::TargetExternalSymbol ||
      Addr.getOpcode() == ISD::TargetGlobalAddress ||
      Addr.getOpcode() == ISD::TargetGlobalTLSAddress) {
    return false; // direct calls.
  }

  Base = Addr;
  Offset = CurDAG->getTargetConstant(0, Addr, MVT::i32);
  return true;
}

void SPCPUDAGToDAGISel::Select(SDNode *N) {
  SelectCode(N);
}

/// createSPCPUISelDag - This pass converts a legalized DAG into a
/// SPCPU-specific DAG, ready for instruction scheduling.
///
FunctionPass *llvm::createSPCPUISelDag(SPCPUTargetMachine &TM,
                                     CodeGenOpt::Level OptLevel) {
  return new SPCPUDAGToDAGISel(TM, OptLevel);
}
